# ------------------------------------------------------------------------------
# 函数名: check_net_connectivity
# 功能:   获测试网络连通性
# 
# 参数:
#   无
# 
# 返回值:
#   成功: 正常
#   失败: 异常
# 
# 示例:
#   check_net_connectivity
# ------------------------------------------------------------------------------
check_net_connectivity() {
    # 测试 8.8.8.8 是否可达
    if ping -c 1 -W 1 8.8.8.8 &>/dev/null; then
        print_echo "正常"
    else
        print_echo "异常"
    fi
}
