#!/usr/bin/env bash

# ==============================================================================
# VpsScriptKit - 系统更新
#
# @文件路径: modules/system/system/update.sh
# @功能描述: 自动识别当前 Linux 系统可用的包管理器，并执行系统更新/升级
#
# @作者: Jamison
# @版本: 1.0.0
# @创建日期: 2026-01-12
# ==============================================================================

guard_system_update(){
    print_clear

    if system_update; then
        print_box_success -s finish -m "更新系统"
    else
        print_box_error -m "系统更新过程中出现错误"
    fi
}

# ------------------------------------------------------------------------------
# 函数名: system_update
# 功能:   执行系统软件包更新与升级（行为型）
# 
# 参数:
#   无
# 
# 返回值:
#   0 - 系统更新执行成功
#   1 - 系统不支持或更新过程中发生错误
# 
# 示例:
#   system_update
# ------------------------------------------------------------------------------
system_update() {
    local pkg_manager
    pkg_manager="$(os_get_pkg_manager)" || {
        # 如果无法识别包管理器，直接返回失败
        print_error -m "当前系统未检测到支持的包管理器，无法执行系统更新"
        return 1
    }

    print_box_info -s start -m "更新系统"
    # --------------------------------------------------------------------------
    # 根据不同包管理器，执行对应的系统更新逻辑
    # 所有命令均使用非交互模式，避免阻塞脚本执行
    # --------------------------------------------------------------------------
    case "$pkg_manager" in

        # ----------------------------------------------------------------------
        # Debian / Ubuntu 系列
        # ----------------------------------------------------------------------
        apt)
            # 修复 dpkg 中断状态（防止清理失败）
            fix_dpkg

            # 使用非交互环境变量，避免 tzdata 等包阻塞
            if ! DEBIAN_FRONTEND=noninteractive apt update -y; then
                print_error -m "apt 更新软件源失败"
                return 1
            fi

            # 执行完整升级（允许安装/移除依赖）
            if ! DEBIAN_FRONTEND=noninteractive apt full-upgrade -y; then
                print_error -m "apt 系统升级失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # RHEL 8+ / Fedora
        # ----------------------------------------------------------------------
        dnf)
            # 执行系统更新
            if ! dnf -y update; then
                print_error -m "dnf 系统更新失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # RHEL 7 / CentOS 7
        # ----------------------------------------------------------------------
        yum)
            # 执行系统更新
            if ! yum -y update; then
                print_error -m "yum 系统更新失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # Alpine Linux
        # ----------------------------------------------------------------------
        apk)
            # 更新软件包索引
            if ! apk update; then
                print_error -m "apk 更新索引失败"
                return 1
            fi

            # 升级所有已安装包
            if ! apk upgrade; then
                print_error -m "apk 系统升级失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # Arch Linux
        # ----------------------------------------------------------------------
        pacman)
            # 同步并升级所有软件包
            if ! pacman -Syu --noconfirm; then
                print_error -m "pacman 系统更新失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # openSUSE
        # ----------------------------------------------------------------------
        zypper)
            # 刷新软件源
            if ! zypper refresh; then
                print_error -m "zypper 刷新软件源失败"
                return 1
            fi

            # 执行系统升级
            if ! zypper update -y; then
                print_error -m "zypper 系统更新失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # OpenWrt / 嵌入式系统
        # ----------------------------------------------------------------------
        opkg)
            # 更新软件源
            if ! opkg update; then
                print_error -m "opkg 更新软件源失败"
                return 1
            fi

            # 升级所有可升级包
            if ! opkg upgrade; then
                print_error -m "opkg 系统更新失败"
                return 1
            fi
            ;;

        # ----------------------------------------------------------------------
        # 未支持的包管理器（理论上不会进入）
        # ----------------------------------------------------------------------
        *)
            print_error -m "当前包管理器不受支持: $pkg_manager"
            return 1
            ;;
    esac

    return 0
}
